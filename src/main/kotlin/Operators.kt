// Arithmetic Operators

fun main (){
    var num1:Int = 25
    var num2:Int = 6

    println(num1+num2)
    println(num1-num2)
    println(num1*num2)
    println(num1/num2)
    println(num1%num2) // Modulus

    var x = 5
    println(x)

    var y = 3
    y +=7
    println(y)

    var a = 78
    a -=5
    println(a)
    // Comparison Operators

    var number:Int = 100
    println(number<20)
    println(number>10)
    println(number==30)
    println(number<=30)
    println(number>=30)
    println(number!=30)

    //Logical Operators

    var myNumber = 90
    println(myNumber<100 && myNumber==30) //And
    println(myNumber<100 || myNumber==30) //Or
    println(!(myNumber<100 || myNumber==30)) //Not
}



