fun main(){
    addition(23, 28)
    subtraction(68,29)
    d(81,9)
    multiplication(7,8)
}

fun addition(num1:Int, num2:Int){
    println(num1 + num2)
}

fun subtraction(num1:Int, num2:Int){
    println(num1-num2)
}

fun d(num1:Int, num2:Int){
    println(num1/num2)
}

fun multiplication(num1:Int, num2:Int){
    println(num1*num2)
}